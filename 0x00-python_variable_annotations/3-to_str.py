#!/usr/bin/env python3


"""
Module: 3-to_str

This module provides a function for converting a float to str
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Args:
        n (float): The input float.

    Returns:
        str: The string representation of the input float.

    Example:
        >>> to_str(3.14)
        '3.14'
    """
    return str(n)
