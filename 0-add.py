#!/usr/bin/env python3


"""
Module: 0-add

This module provides a function for adding two float numbers.

"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two input numbers.

    Example:
        >>> add(1.11, 2.22)
        3.33
    """
    return a + b
